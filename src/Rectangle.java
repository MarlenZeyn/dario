public class Rectangle {
    int a , b;

    public Rectangle() {
        this.a = a;
        this.b = b;
    }
    public int findPer(int a, int b) {
        int per = 2*a+2*b;
        return per;
    }

    public int findArea(int a, int b) {
         int area = a*b;
        return area;
    }



}
